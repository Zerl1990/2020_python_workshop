# Dictionary have key: value pairs
my_dictionary = {
    'first_key': 15,
    'second_key': True,
    'third_key': 'test'
}


# Print dictionary information
print(f'My Dictionary: {my_dictionary}')
print(f"Access value with first_key: {my_dictionary['first_key']}")

# Modify value in dictionary
my_dictionary['first_key'] = 5
print(f"Value after modification: {my_dictionary['first_key']}")

