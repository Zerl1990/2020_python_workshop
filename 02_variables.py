# type function return the type of the variables.
# for example, for number variable, it will return int

number = 5
print("Number Type:")
print(type(number))

decimal = 15.5
print("Decimal Type:")
print(type(decimal))

char = 'A'
print("Char Type:")
print(type(char))

string = 'My String'
print("String Type:")
print(type(string))

boolean = True
print("Boolean Type:")
print(type(boolean))

arr = [0, 1, 2, 3]
print("List Type:")
print(type(arr))



